class Circle {
    public static void main(String args[]) {
        double r = 3.8, area, circumference;
        circumference = 2 * 3.14159 * r;
        area = 3.14159 * r * r;
        System.out.println("Circumference of circle is: " + circumference);
        System.out.println("Area of circle is: " + area);
    }
}
